
public class Veiculo {
	private long codigo;
	private int ano;
	private double valor;

	public long getCodigo() {
		return codigo;
	}

	public int getAno() {
		return ano;
	}

	public double getValor() {
		return valor;
	}

	public void setCodigo(long codigo) {
		this.codigo = codigo;
	}

	public void setAno(int ano) {
		this.ano = ano;
	}

	public void setValor(double valor) {
		this.valor = valor;
	}
}
