
public class Documento {
	private long numero;
	private String emissao;

	public long getNumero() {
		return numero;
	}

	public String getEmissao() {
		return emissao;
	}

	public void setNumero(long numero) {
		this.numero = numero;
	}

	public void setEmissao(String emissao) {
		this.emissao = emissao;
	}
}
